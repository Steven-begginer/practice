while True:
    n = int(input("What is n? "))
    if n < 0:
        print(n)
        continue #Python's continue keyword functions as a statement that controls the flow of a loop. It allows you to skip code in a loop for the current iteration and jump immediately to the next one.
    else:
        break # break as a statement will terminate the whole loop.
# when while is equal to false, the loop will stop. Before that, the loop will continue  to work  forever.



